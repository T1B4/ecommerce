    public function insert() {
        $data = [];

        $categories = new Categories();

        if (isset($_POST['name'])) {

            if (isset($_POST['category']) && !empty($_POST['category'])) {
                $sub = filter_input(INPUT_POST, 'category', FILTER_SANITIZE_NUMBER_INT);
                $data['mensagem']['subcategoria'] = "Categoria inserida com sucesso!";
            } else {
                $sub = null;
                $data['mensagem']['principal'] = "Categoria inserida com sucesso!";
            }
            $name = filter_input(INPUT_POST, 'name', FILTER_SANITIZE_STRING);
            $slug = $this->criar_slug($name);
            $categories->insert($sub, $name, $slug);
        }

        $data['categories'] = $categories->getList();

        $this->loadTemplate('categories_insert', $data);
    }

    public function edit($id = '') {
        if (!isset($id) || empty($id)) {
            header("Location: " . BASE_PAINEL . "category/see_all");
            exit;
        }

        $data = [];
        $categories = new Categories();

        if (isset($_POST['id']) && !empty($_POST['id'])) {

            $id = filter_input(INPUT_POST, 'id', FILTER_SANITIZE_NUMBER_INT);
            $name = filter_input(INPUT_POST, 'name', FILTER_SANITIZE_STRING);
            $slug = $this->criar_slug($name);
            $categories->edit($id, $name, $slug);

            $data['mensagem'] = "Categoria alterada com Sucesso!";
        }

        $data['cat'] = $categories->getCategory($id);

        $this->loadTemplate('categories_edit', $data);
    }

    public function del($id) {
        $data = [];

        if (isset($id) && !empty($id)) {

            $cat = new Categories();
            $cat->delCategory($id);
        }

        header("Location: " . BASE_PAINEL . "category/see_all");
